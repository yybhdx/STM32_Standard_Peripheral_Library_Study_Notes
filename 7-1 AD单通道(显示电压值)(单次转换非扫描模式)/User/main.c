#include "stm32f10x.h"                  // Device header
#include "Delay.h"
#include "OLED.h"
#include "AD.h"

uint16_t ADValue; // 用于存储ADC的转换结果值

float Voltage;

int main(void)
{
	/*模块初始化*/
	OLED_Init();		//OLED初始化
	
	AD_Init(); // ADC初始化
	
	OLED_ShowString(1, 1, "ADValue:");
	
	OLED_ShowString(2, 1, "Voltage:0.00V");
	
	while (1)
	{
		// 获取ADC的转换结果值
		ADValue = AD_GetValue();
		
		// 计算得到电压值
		// 这样就能把0-4095的范围，变换到0~3.3了
		// 另外这里ADValue是整数，再除4095之后，会舍弃掉小数部分
		// 这样会导致计算错误
		// 所以我们先把ADValue类型强转为float
		// 还有，实际上AD值等于4096时，才对应3.3V，会有一个数的偏差
		// 所以AD值最大的4095实际上对应的应该是3.3V小一丢丢，没有办法达到满量程3.3V(固有量化误差)
		// 这个是受限于ADC的结果
		// 总之就是，你认为4095对应3.3V可以，认为4096对应3.3V也可以
		// 只有一点点偏差，也看不出来区别
		Voltage = (float)ADValue / 4095 * 3.3;
		
		// 在OLED的1行九列显示ADValue的值
		OLED_ShowNum(1, 9, ADValue, 4); 
		// 显示的ADValue值的末尾会有些抖动，这是正常的波动
		/* 如果你想对这个值进行判断，再执行一些操作
		比如光线的AD值小于某一阈值，就开灯，大于某一阈值，就关灯
		那可能会存在这样的情况:比如光线逐渐变暗，AD值逐渐变小
		但是由于波动，AD值会在判断阈值负极来回跳变
		这会导致输出产生抖动，来回开灯关灯开灯关灯*/
		/*那如何避免这种情况??
		这个可以使用迟滞比较的方法来完成
		设置两个阈值，低于下阈值时，开灯
		高于上阈值时才关灯，这就可以避免输出抖动的问题了
		这个跟GPIO那一节讲的施密特触发器是一个原理*/
		/*另外，如果你觉得数据跳变太厉害
		还可以采用滤波的方法，让AD值平滑一些
		比如均值滤波，就是读10个或20个值，取平均值，作为滤波的AD值
		或者还可以裁剪分辨率，把数据的尾数去掉
		这样也可以减少数据波动*/
		
		// 显示Voltage(电压)(浮点数)的值
		// 在OLED的2行九列显示Voltage(电压)的整数值
		/**
		* @brief  OLED显示数字（十进制，正数）
		* @param  Line 起始行位置，范围：1~4
		* @param  Column 起始列位置，范围：1~16
		* @param  Number 要显示的数字，范围：0~4294967295
		* @param  Length 要显示数字的长度，范围：1~10
		* @retval 无
		*/
		OLED_ShowNum(2, 9, Voltage, 1); 
		
		// 在OLED的2行11列显示Voltage(电压)的小数部分
		// 由于Voltage是浮点数不能取余，所以要对Voltage * 100后进行强制类型转换
		OLED_ShowNum(2, 11, (uint16_t)(Voltage * 100) % 100, 2); 
		
		Delay_ms(100);
	}
}

//为什么 (uint16_t)(Voltage * 100) 要将Voltage强制转换为uint16_t 而不是uint32_t??
//?? 为什么是 uint16_t而不是 uint32_t？
//核心原因在于取值范围匹配和内存效率。
//?取值范围完美匹配?：STM32的ADC是12位精度，这意味着它转换得到的原始值范围是 ?0 到 4095? (2^12 - 1) 。这个范围完全落在 uint16_t（0 到 65,535）的表示范围内。使用 uint16_t存储ADC原始结果是标准且安全的做法。
//?避免资源浪费?：uint16_t类型变量占用 2 个字节的内存，而 uint32_t占用 4 个字节。对于 ADValue这个变量来说，它的值永远不会超过4095，使用 uint32_t会造成内存空间的浪费，尤其是在资源紧张的嵌入式环境中，这种“精打细算”是有意义的。
//所以，对于存储ADC原始值，uint16_t是恰到好处的选择。
//?? 为什么不能直接使用 float类型？
//这是因为你使用的 OLED_ShowNum函数（根据其参数 uint32_t num推断）被设计为显示整数，而不是浮点数?。它期望接收一个整型的数字，而不是一个浮点数。
//如果你直接传入 Voltage * 100这个浮点数，C语言会发生隐式类型转换，但结果可能不是你想要的。更规范、更安全的方式是你代码中使用的显式类型转换? (uint16_t)(Voltage * 100)。
//?你的代码?：(uint16_t)(Voltage * 100)明确地告诉编译器：“请将 Voltage * 100的结果截断小数部分，转换成一个16位的无符号整数”。
//?小数部分处理?：类型转换的本质是截断?（直接舍弃小数部分），而不是四舍五入。
//例如，3.14159转换为整数后就是 3。如果你希望实现四舍五入，需要在转换前手动加 0.5。


//我明明使用的是单次转换非扫描的模式,为什么我的LCD却可以源源不断地显示新的数据???
//?? 详解“单次”与“连续”的真实含义
//结合流程图，我们再来细看代码中如何实现这种“伪连续”采集：
//?1.ADC配置为单次模式?
//在你的初始化函数 AD_Init()中，关键配置是 ADC_InitStruct.ADC_ContinuousConvMode = DISABLE。
//这意味着每次触发后，ADC只对一个通道执行一次转换，然后自动停止并等待下一次触发?。它不会自己周而复始地运行。

//??2.“引擎”在主循环中?
//秘密在于 main()函数里的 while(1)循环。在这个死循环里，你反复执行 ADValue = AD_GetValue();。
//我们来看 AD_GetValue()这个函数内部做了什么：
//ADC_SoftwareStartConvCmd(ADC1, ENABLE);：?通过软件命令，手动触发了一次ADC转换?。

//while(ADC_GetFlagStatus(ADC1, ADC_FLAG_EOC) == RESET);：等待这次单次转换完成。

//return ADC_GetConversionValue(ADC1);：读取转换结果。

//所以，整个过程的本质是：?你的主循环在不停地“点射”?。每次循环都命令ADC“打一枪”（单次转换），然后等它报靶（EOC标志），
//记录环数（读取数据），歇一会儿（Delay_ms(100)），接着再命令“打一枪”。
//因为你点射的速度非常快（循环周期约100ms+转换时间），从人的感官上看，LCD上的数据就是在连续不断地更新了。
