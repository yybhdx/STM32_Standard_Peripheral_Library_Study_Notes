#include "stm32f10x.h"                  // Device header
#include "Delay.h"
#include "OLED.h"

// 被Const修饰的变量只能读 不能写(Flash里面的数据也是只能读，不能写的，所以Const和Flash就联系起来了)，在STM32中，使用Const存储的变量，是存储在flash里面的
// 当然这里就不应该说是"变量"了，而应该说是"常量"，因为它是不能"变"的
const uint8_t aa = 0x66;

int main(void)
{
	/*模块初始化*/
	OLED_Init();		//OLED初始化
	
	/*OLED显示*/
	/**
  * @brief  OLED显示数字（十六进制，正数）
  * @param  Line 起始行位置，范围：1~4
  * @param  Column 起始列位置，范围：1~16
  * @param  Number 要显示的数字，范围：0~0xFFFFFFFF
  * @param  Length 要显示数字的长度，范围：1~8
  * @retval 无
  */
	// 显示16进制数 0x66这个八位的数据 在16进制里面是2位
	OLED_ShowHexNum(1, 1, aa, 2); // 66
	
	// 显示16进制数32位的地址在16进制里面是4位
	// 变量aa不加const被存储在 2000 0000地址(存储在了运行内存SRAM里)，因为是16进制，所以不显示0X
	// 变量aa加上const被存储在 0800 0000地址(存储在了程序存储器Flash里)，因为是16进制，所以不显示0X
	OLED_ShowHexNum(2, 1, (uint32_t)&aa, 8); // 0800 0E10
	
	// 查看外设的结构体地址
	// ADC_DR寄存器ADC1->DR
	// ADC外设寄存器的起始地址为0X4000 0000 ADC_DR寄存器的偏移地址为4C
	OLED_ShowHexNum(3, 1, (uint32_t)&ADC1->DR, 8); // 4001 244C // 这个地址4001 244C是固定的，在手册里也能查到
	
	while (1)
	{
		
	}
}
//OLED显示aa的取地址(&aa)时为什么要强制转换成uint32_t位??
//在STM32编程中，使用OLED_ShowHexNum函数显示变量地址时，将其强制转换为uint32_t类型是为了满足函数参数类型要求，避免编译器警告或错误，并确保地址值被正确解释和显示。
//下面是一个更详细的解释：
//原因说明					详细解释
//​函数参数类型要求​
//OLED_ShowHexNum函数用于显示十六进制数。它的第三个参数期望一个uint32_t类型的数字，而&aa（取aa的地址）是一个指针类型（uint8_t*）。

//​避免编译器警告/错误​
//如果不进行强制类型转换，直接将指针传递给期望uint32_t参数的函数，会导致数据类型不匹配，编译器可能会产生警告或错误。

//​地址的本质​
//在STM32的32位系统中，地址本身是一个32位的无符号整数。强制转换为uint32_t可以明确地将指针值转换为一个整数类型，以便函数能够正确处理和显示它。

//​保持代码可移植性​
//虽然STM32的地址是32位，但显式转换可以使代码意图更清晰，并在不同平台间保持更好的可移植性（尽管STM32是固定的32位架构）。
